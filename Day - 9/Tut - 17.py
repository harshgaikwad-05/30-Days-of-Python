# Learning about the For Loops in Python

# Foe loops can iterate over a sequence of iterable objects in python.

name = "Albert"
for i in name:
    print(i)

list1 = ["orange", "red", "blue", "purple"]
for color in list1:
    print(color)

for k in range(5):
    print(k + 1)

