# ==============================
# Python Lists - Basics
# ==============================

# Creating a list
list1 = [3, 4, 5, 6]

# Printing the entire list
print("Full list:")
print(list1)

print("\nAccessing elements using index:")

# Indexing (index starts from 0)
print("Element at index 0:", list1[0])
print("Element at index 1:", list1[1])
print("Element at index 2:", list1[2])
print("Element at index 3:", list1[3])


# ==============================
# List with different data types
# ==============================

list1 = [3, 4, 5, 6, "John", True]

print("\nList with mixed data types:")
print(list1)


# ==============================
# List Slicing
# ==============================

# Syntax:
# list[start : stop : step]

print("\nUsing slicing:")
print("Complete list using [:]:", list1[:])


# ==============================
# Accessing elements again
# ==============================

print("\nAccessing elements again:")
print("Index 0:", list1[0])
print("Index 1:", list1[1])
print("Index 2:", list1[2])
print("Index 3:", list1[3])
